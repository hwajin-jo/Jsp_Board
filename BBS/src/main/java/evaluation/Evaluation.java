package evaluation;

public class Evaluation {
	private int bbsID;
	private String userID;
	private int likeit;
	private int badit;
	public int getBbsID() {
		return bbsID;
	}
	public void setBbsID(int bbsID) {
		this.bbsID = bbsID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public int getLikeIt() {
		return likeit;
	}
	public void setLikeIt(int likeit) {
		this.likeit = likeit;
	}
	public int getBadIt() {
		return badit;
	}
	public void setBadIt(int badit) {
		this.badit = badit;
	}
	
	
}